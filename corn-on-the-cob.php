                <h2>Ingredients: serves 4 </h2>

                <ul class="ingredients">
                    <li>4 sweet corn cobs, husked.</li>
                    <li>2 tablespoons sugar</li>
                    <li>2 tablespoons vinegar</li>
                    <li>1 tablespoon butter</li>
                    <li>cold water</li>
                </ul>
				</div>
				<div class="row align-left">
                <h2>Directions</h2>
                <p>
                    Use cold water in a large pot. For every four whole cobs of corn add 2 Tablespoons sugar and 2 Tablespoons vinegar to the water.</p>
                <p>
                    Bring water to a boil uncovered. Add the corn on the cob. Cook for about 6 minutes, uncovered. Reduce heat to simmer / low.
                </p>
                <p>
                    Add 1 tablespoon butter. Cover pot and allow time for the butter to melt.</p>

                <p>
                    Serve it at any time. Leaving the corn not served in the water simmering, it will stay fresh for a few hours.
                </p>
