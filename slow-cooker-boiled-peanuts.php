                <h1>Slow Cooker Boiled Peanuts</h1>
				
				<div class="align-left intro-text">
					<p>Cooking time varies between 10 - 18 hours.</p>
                    				
					<p>This vegetarian recipe is meant to be easy, flavorful and healthy. It's basic and designed in a way to 
					reduce the amount of salt and remain tasty.</p>
                </div>

                <div class="align-left">
					<h2>About <span style="color:#ccc;">Boiled Peanuts</span></h2>
					<p>Boiled peanuts are popular in some places where peanuts are common. Fully mature peanuts do not make good 
					quality boiled peanuts; rather raw or "green" ones are used. "Raw" denotes peanuts in a semi-mature state, having achieved full size, 
					but not being fully dried, as would be needed for roasting or peanut butter use. After boiling in salt water they take on a 
					strong salty taste and become softer with the length of cooking, somewhat resembling a pea or bean, to which they are related. 
					The most flavorful peanuts for boiling are the Valencia type. These are preferred in the United States, being grown in 
					gardens and small patches throughout the South. Green Virginia-type peanuts are also sometimes used. <a href="https://en.wikipedia.org/wiki/Boiled_peanuts" style="font-size:11Spx;color:#cdcdcd;"> [wikipedia]</a></p>
					
                </div>
            </div>

            <div class="row align-left">

                <h2>Ingredients</h2>

                <ul class="ingredients">
                  <li>1 lb Raw Peanuts (not Raw Green Peanuts), Green peanuts aren't a separate variety. They're just peanuts of any type that are harvested early and sold while they're still moist. You can only get them during the growing season.</li>
                  <li>1 tbl Garlic Powder</li>
                  <li>1 tbl Chili Powder</li>
                  <li>1 tsp Clove Spice</li>
                  <li>¼ cup Salt</li>
                  <li>3 oz Crab Boil (in pouch)</li>
                  <li>3 Quart Crock Pot</li>
				  <li>Water</li>
                </ul>				
            </div>
			<div class="row align-left">
			<h2>Directions</h2>
                <p>Put the pouch of crab boil in the bottom of the crock pot.</p>
				<p>Add peanuts.</p>
				<p>Add remaining ingredients.</p>
                <p>Add enough water to cover peanuts (some will float, don't over fill crock pot with water, leave a little room for the peanuts to expand).</p>
				<p>Stir to mix.</p>
                <p>Cover and cook on low for 10 to 18 hours, or longer depending on the preference of doneness.</p>
                <p>Check on the peanuts from time to time and give them a good stir to mix and help with even cooking of the batch.</p>
				<p>You may need to add more water due to evaporation before they are finished cooking.</p>
				<p>During the later stages of cooking, taste a few to check for doneness. They should be soft. (softness to your preference) adjust your cooking time accordingly.</p>
				<p>For spicier peanuts add a few peppers like serrano, jalapeño or red pepper flakes to taste.</p>
				<p>When done let them cool enough to handle before serving.</p>
				<p>These are great appetizers and snacks.</p>
				<p>Boiled peanuts should be kept refrigerated and will keep for about 7 days.</p>
			
			</div>
